/** Image compression for talent uploads (client-side). */
async function compressImageIfNeeded(file: File) {
  if (!file.type.startsWith("image/")) return file

  const MAX_MB = 1.2
  const maxBytes = MAX_MB * 1024 * 1024
  if (file.size <= maxBytes) return file

  const img = document.createElement("img")
  const url = URL.createObjectURL(file)
  img.src = url

  await new Promise((res, rej) => {
    img.onload = () => res(true)
    img.onerror = rej
  })

  const canvas = document.createElement("canvas")
  const ctx = canvas.getContext("2d")!
  const maxW = 1600
  const scale = Math.min(1, maxW / img.width)
  canvas.width = Math.round(img.width * scale)
  canvas.height = Math.round(img.height * scale)

  ctx.drawImage(img, 0, 0, canvas.width, canvas.height)

  return new Promise<File>((resolve, reject) => {
    canvas.toBlob(
      (blob) => {
        if (!blob) {
          reject(new Error("Canvas blob conversion failed"))
          return
        }
        URL.revokeObjectURL(url)
        const fileName = file.name.replace(/\.\w+$/, "") + ".jpg"
        const fileObj = new (globalThis as any).File([blob], fileName, { type: "image/jpeg" })
        resolve(fileObj)
      },
      "image/jpeg",
      0.72
    )
  })
}

/** Upload to RAW path (talent-only readable). Watermark previews are generated server-side. */
async function uploadMilestoneRawFiles(wsId: string, milestoneId: string, rawFiles: File[]) {
  const out: MilestoneAttachment[] = []

  for (const f of rawFiles) {
    const MAX_SIZE_MB = 25
    if (f.size > MAX_SIZE_MB * 1024 * 1024) throw new Error(`"${f.name}" is too large. Max ${MAX_SIZE_MB}MB`)

    const fileToUpload = await compressImageIfNeeded(f)
    const safeName = fileToUpload.name.replace(/[^\w.\-]+/g, "_")
    const path = `workspaces/${wsId}/milestones/${milestoneId}/raw/${Date.now()}_${safeName}`

    // Debug: log upload target path and file info
    try {
      console.log("[uploadMilestoneRawFiles] uploading to:", path, { name: fileToUpload.name, size: fileToUpload.size, contentType: fileToUpload.type })
    } catch (err) {
      /* ignore logging errors */
    }

    await uploadBytes(storageRef(storage, path), fileToUpload, {
      contentType: fileToUpload.type || "application/octet-stream",
    })

    // Don't store raw download URLs on doc (client must never access raw).
    out.push({
      kind: "raw",
      name: fileToUpload.name,
      contentType: fileToUpload.type || "application/octet-stream",
      size: fileToUpload.size,
      storagePath: path,
    })
  }

  return out
}

